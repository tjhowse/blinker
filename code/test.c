uint16_t rotate_bitmap(uint16_t bitmap) {
    uint8_t rotationMap[] = {
        14, 11, 8, 5, 2,
        13, 10, 7, 4, 1,
        12, 9, 6, 3, 0
    };
    uint16_t rotated = 0;
    for (int i = 0; i <= 15; i++) {
        // printf("Offsetting %d by %d: %d\n", i, rotationMap[i], (bitmap >> (rotationMap[i])) & 0x1);
        if ((bitmap >> (rotationMap[i]+1)) & 0x1) {
            rotated |= (1 << (14-i));
        }
    }
    return rotated;
}

